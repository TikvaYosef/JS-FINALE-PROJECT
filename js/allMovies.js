function showOnScreen(object) {

    for (const item of object) {
        allMoviesDiv.innerHTML += `<div class="card"> <h4 class="title">${item.movieName}</h4> <p class = "rating">rating ${item.rating}</p> <img src ="${item.image}" class="imgMovies"></div>`
        
    }

}

async function allMovies() {
    try {
        return await fetch('https://moviesmern.herokuapp.com/movies/all')
            .then((response) => response.json())

    }
    catch (error) {
        return error

    }

}

allMovies()
    .then(res => showOnScreen(res.data))
    .catch(rej => console.log(rej))




